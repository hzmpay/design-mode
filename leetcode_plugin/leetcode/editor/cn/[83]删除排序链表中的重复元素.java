/**
 * Definition for singly-linked list.
 * public class ListNode {
 *     int val;
 *     ListNode next;
 *     ListNode() {}
 *     ListNode(int val) { this.val = val; }
 *     ListNode(int val, ListNode next) { this.val = val; this.next = next; }
 * }
 */
class Solution {
    public ListNode deleteDuplicates(ListNode head) {
        if (head == null) {
            return null;
        }
        // å®šä¹‰åŒæŒ‡é’ˆ
        ListNode pre = head;
        ListNode cur = head.next;
        while (cur != null) {
            // å½“å‰å…ƒç´ ä¸å‰ä¸€ä¸ªèŠ‚ç‚¹ç›¸ç­‰ï¼Œå°†å½“å‰èŠ‚ç‚¹åˆ é™¤
            if (pre.val == cur.val) {
                pre.next = cur.next;
                cur = cur.next;
            } else {
                pre = cur;
                cur = cur.next;
            }
        }
        return head;
    }
}
